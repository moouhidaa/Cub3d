#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*str;
	int		lenn;

	if (!s || !f)
		return (NULL);
	i = 0;
	lenn = ft_strlen(s) + 1;
	str = (char *)malloc((lenn) * sizeof(char));
	if (!str)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
